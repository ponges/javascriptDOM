// DOM Manipulation

//getElementById()
const title = document.getElementById('main-heading')
console.log(title)
// GetElementByClassName()
const listItemClass = document.getElementsByClassName('list-items')
console.log(listItemClass)
// GetElementsByTagName()
const listItemsTag = document.getElementsByTagName('li')
console.log(listItemsTag)
// querySelector() - selects first matching value it finds
const container1 = document.querySelector('div')
console.log(container1)
// querySelectorAll()
const container2 = document.querySelectorAll('div')
console.log(container2)
